class ArticleSource {
  String id;
  String name;

  ArticleSource({this.id, this.name});
}

class Article {
  ArticleSource source;
  String author;
  String title;
  String description;
  String url;
  String urlToImage;
  String publishedAt;
  String content;

  Article(
      {this.source,
      this.author,
      this.title,
      this.description,
      this.url,
      this.urlToImage,
      this.publishedAt,
      this.content});
}
